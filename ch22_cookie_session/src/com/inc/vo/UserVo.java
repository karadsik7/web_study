package com.inc.vo;

public class UserVo {

	private String id, password, name;

	public UserVo(String id, String password, String name) {
		this.id = id;
		this.password = password;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public String getPassword() {
		return password;
	}

	public String getName() {
		return name;
	}
	
	
	
	
	
	
	
}
